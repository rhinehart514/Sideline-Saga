export const getFCSTeamById = (id: string): Team | undefined => {
  return fcsTeams.find(team => team.id === id);
};

export const getFCSTeamsByConference = (conference: string): Team[] => {
  return fcsTeams.filter(team => team.conference === conference);
};

export const getTopFCSPrograms = (): Team[] => {
  return fcsTeams.filter(team => team.prestige >= 4);
};
