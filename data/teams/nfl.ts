// ============================================
// LOOKUP HELPERS
// ============================================

export const getNFLTeamById = (id: string): NFLTeam | undefined => {
  return nflTeams.find(team => team.id === id);
};

export const getNFLTeamsByConference = (conference: 'AFC' | 'NFC'): NFLTeam[] => {
  return nflTeams.filter(team => team.conference === conference);
};

export const getNFLTeamsByDivision = (conference: 'AFC' | 'NFC', division: string): NFLTeam[] => {
  return nflTeams.filter(team => team.conference === conference && team.division === division);
};

export const getSuperBowlWinners = (): NFLTeam[] => {
  return nflTeams.filter(team => team.superBowlWins > 0);
};
